    /**
     * post请求（用于请求json格式的参数）
     * @param url
     * @param params
     * @return
     */
    public static String postJson(String url, String params) throws IOException {

        CloseableHttpClient httpclient = HttpClients.createDefault();
        HttpPost httpPost = new HttpPost(url);// 创建httpPost
        httpPost.setHeader("Accept", "application/json");
        httpPost.setHeader("Content-Type", "application/json");
        String charSet = "UTF-8";
        StringEntity entity = new StringEntity(params, charSet);
        httpPost.setEntity(entity);
        CloseableHttpResponse response = null;

        try {

            response = httpclient.execute(httpPost);
            StatusLine status = response.getStatusLine();
            int state = status.getStatusCode();
            if (state == HttpStatus.SC_OK) {
                HttpEntity responseEntity = response.getEntity();
                String jsonString = EntityUtils.toString(responseEntity);
                return jsonString;
            }
            else{
                logger.error("请求返回:"+state+"("+url+")");
            }
        }
        finally {
            if (response != null) {
                try {
                    response.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            try {
                httpclient.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        return null;
    }

    /**
     * 以post请求方式发送文件
     * @param url
     * @param params 参数
     * @param fileInput 文件的输入流
     * @return 默认返回参数为Json格式的数据
     * @throws IOException
     * @throws InterruptedException
     */
    static public String postFile(String url, InputStream fileInput,String fileName, Map<String,String> params) throws IOException {
        CloseableHttpClient httpclient = HttpClients.createDefault();
        CloseableHttpResponse response = null;
        String result = null;
        try {
            HttpPost httpPost = new HttpPost(url);

            MultipartEntityBuilder mEntityBuilder = MultipartEntityBuilder.create();
//            mEntityBuilder.addBinaryBody("files", fileInput);
            mEntityBuilder.addBinaryBody("files",fileInput, ContentType.create("multipart/form-data"), fileName);
            Iterator iterator = params.keySet().iterator();
            while (iterator.hasNext()){
                String key = (String) iterator.next();

                StringBody stringBody = new StringBody(params.get(key),ContentType.MULTIPART_FORM_DATA);
                mEntityBuilder.addPart(key,stringBody);
            }

            httpPost.setEntity(mEntityBuilder.build());
            response = httpclient.execute(httpPost);

            int statusCode = response.getStatusLine().getStatusCode();
            if (statusCode == HttpStatus.SC_OK) {
                HttpEntity resEntity = response.getEntity();
                result =EntityUtils.toString(resEntity);
                // 消耗掉response
                EntityUtils.consume(resEntity);
            }
        } catch (ParseException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            HttpClientUtils.closeQuietly(httpclient);
            HttpClientUtils.closeQuietly(response);
        }
        return result;
    }

    static public String postFile(String url, File fileInput, Map<String,String> params) throws IOException {
        return postFile(url,new FileInputStream(fileInput),fileInput.getName(),params);
    }

